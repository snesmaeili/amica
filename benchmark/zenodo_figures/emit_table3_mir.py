"""Main Table 3 -- exact paired within-subject MIR effects, generated from estimates_realeeg.csv.

9 rows (3 datasets x 3 comparators), one effect per contrast (NOT averaged). Columns: dataset, n,
comparator, paired mean DeltaMIR (95% CI), Cohen's d_z (95% bootstrap CI), paired-t p, Holm-p,
Wilcoxon p, same-sign count. Writes tab_mir_effects.tex. Every value traces to the committed CSV.
"""
from __future__ import annotations

import math
from pathlib import Path

import pandas as pd

HERE = Path(__file__).resolve().parent
DS = [("ds004505", 25), ("ds004504", 29), ("ds004621", 42)]   # display order
COMPS = ["Picard", "Infomax", "FastICA"]


def sci(p):
    if p <= 0:
        return "$<\\!10^{-300}$"
    e = int(math.floor(math.log10(p)))
    a = p / 10 ** e
    return f"${a:.1f}{{\\times}}10^{{{e}}}$"


def main():
    m = pd.read_csv(HERE / "estimates_realeeg.csv")
    m = m[m.metric == "mir_kbits_s"]
    lines = [
        "% Auto-generated by emit_table3_mir.py from estimates_realeeg.csv -- do not edit by hand.",
        "\\begin{table*}[t]",
        "\\centering",
        "\\caption{\\textbf{Exact paired within-subject MIR effects.} "
        "$\\Delta$MIR $=$ \\texttt{amica-python} $-$ comparator (kbits/s); positive favours "
        "\\texttt{amica-python}. One row per dataset$\\,\\times\\,$comparator contrast, reported per "
        "contrast (not averaged across comparators). 95\\% confidence intervals are bootstrap "
        "($10{,}000$ resamples); $d_z$ is the paired-difference standardized effect with bootstrap CI; "
        "$p$-values are the paired $t$-test and Wilcoxon signed-rank test. Holm$\\,p$ is "
        "Holm--Bonferroni-adjusted across the family of nine MIR contrasts. ``Same-sign'' counts "
        "subjects whose contrast favours \\texttt{amica-python}. Absolute MIR is not comparable across "
        "datasets (it scales with retained rank and channel geometry; sampling rate harmonised to "
        "$250$~Hz), but the within-subject contrast is.}",
        "\\label{tab:replication}",
        "\\footnotesize",
        "\\setlength{\\tabcolsep}{4.5pt}",
        "\\begin{tabular}{llcccccc}",
        "\\toprule",
        "Dataset & Comparator & $\\Delta$MIR (95\\% CI) & $d_z$ [95\\% CI] & "
        "$t$-test $p$ & Holm $p$ & Wilcoxon $p$ & Same-sign \\\\",
        "\\midrule",
    ]
    for di, (ds, n) in enumerate(DS):
        sub = m[m.dataset == ds].set_index("comparator")
        for j, c in enumerate(COMPS):
            r = sub.loc[c]
            head = (f"\\multirow{{3}}{{*}}{{\\shortstack[l]{{{ds}\\\\$n{{=}}{n}$}}}}"
                    if j == 0 else "")
            dmir = f"$+{r.mean_diff:.3f}\\ ({r.ci_lo:.2f},\\,{r.ci_hi:.2f})$"
            dz = f"${r.dz:.2f}\\ [{r.dz_lo:.2f},\\,{r.dz_hi:.2f}]$"
            lines.append(f"{head} & {c} & {dmir} & {dz} & {sci(r.t_p)} & {sci(r.holm_p)} & "
                         f"{sci(r.wilcoxon_p)} & ${int(r.favours_amica)}/{n}$ \\\\")
        lines.append("\\bottomrule" if di == len(DS) - 1 else "\\midrule")
    lines += ["\\end{tabular}", "\\end{table*}", ""]
    out = HERE / "tab_mir_effects.tex"
    out.write_text("\n".join(lines), encoding="utf-8", newline="\n")
    print(f"wrote {out} ({len(lines)} lines)")


if __name__ == "__main__":
    main()
